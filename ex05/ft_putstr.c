#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	char	d;
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		d = str[i];
		ft_putchar(d);
		i++;
	}
}
